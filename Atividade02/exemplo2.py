# cria variável t e atribui o valor lógico True
t = True
# cria variável f e atribui o valor lógico False
f = False
# exibe na tela o tipo da variável t
print(type(t))
# exibe na tela o resultado da expressão lógica t and f
print(t and f)
# exibe na tela o resultado da expressão lógica t or f
print(t or f)
# exibe na tela o resultado da expressão lógica not t
print(not t)
# exibe na tela o resultado da expressão lógica t diferente de f
print(t != f)